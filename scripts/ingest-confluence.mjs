// scripts/ingest-confluence.mjs
// 1회 수집: Confluence UXDB 페이지 -> data/uxdb-kb.js (window.UXDB_DOCS)
// 사용:  CONFLUENCE_BASE=https://inswave01.atlassian.net/wiki \
//        CONFLUENCE_EMAIL=you@inswave.com \
//        CONFLUENCE_API_TOKEN=xxxx \
//        CONFLUENCE_SPACE=UXDB \
//        node scripts/ingest-confluence.mjs
// 변환만 검증:  node scripts/ingest-confluence.mjs --sample
import fs from "node:fs";
import path from "node:path";

const BASE = (process.env.CONFLUENCE_BASE || "").replace(/\/+$/, "");
const EMAIL = process.env.CONFLUENCE_EMAIL || "";
const TOKEN = process.env.CONFLUENCE_API_TOKEN || "";
const SPACE = process.env.CONFLUENCE_SPACE || "UXDB";
const OUT = path.resolve("data/uxdb-kb.js");
const SAMPLE = process.argv.includes("--sample");

function die(msg){ console.error("ERROR: " + msg); process.exit(1); }

function htmlToText(html){
  return String(html || "")
    .replace(/<h[1-6][^>]*>/gi, "\n\n")
    .replace(/<\s*(br|\/p|\/li|\/h[1-6]|\/tr|\/div)\s*>/gi, "\n")
    .replace(/<[^>]+>/g, " ")
    .replace(/&nbsp;/gi, " ")
    .replace(/&amp;/gi, "&").replace(/&lt;/gi, "<").replace(/&gt;/gi, ">")
    .replace(/&quot;/gi, '"').replace(/&#39;/gi, "'")
    .replace(/[ \t]+/g, " ")
    .replace(/[ \t]*\n[ \t]*/g, "\n")
    .replace(/\n{3,}/g, "\n\n")
    .trim();
}

function chunk(text, title, url){
  const parts = text.split(/\n{2,}/).map(s => s.trim()).filter(s => s.length > 20);
  const list = parts.length ? parts : (text ? [text] : []);
  return list.map(s => {
    const lines = s.split("\n");
    const h = lines[0].trim();
    const heading = (lines.length > 1 && h.length > 0 && h.length <= 120) ? h : title;
    return { heading, text: s.slice(0, 1200), url };
  });
}

function writeKB(docs, meta){
  const js =
    "/* AUTO-GENERATED by scripts/ingest-confluence.mjs — do not edit by hand */\n" +
    "window.UXDB_META = " + JSON.stringify(meta) + ";\n" +
    "window.UXDB_DOCS = " + JSON.stringify(docs) + ";\n";
  fs.mkdirSync(path.dirname(OUT), { recursive: true });
  fs.writeFileSync(OUT, js, "utf8");
  const sec = docs.reduce((n, d) => n + d.sections.length, 0);
  console.log(`wrote ${OUT}: ${docs.length} pages, ${sec} sections`);
}

if (SAMPLE){
  const html = "<h1>UX 작성 가이드</h1><p>버튼 터치 영역은 최소 44x44px를 권장합니다.</p>" +
               "<h2>색 대비</h2><p>본문 텍스트 대비는 4.5:1 이상을 유지합니다.</p>";
  const url = (BASE || "https://example.atlassian.net/wiki") + "/spaces/UXDB/pages/sample";
  const text = htmlToText(html);
  writeKB(
    [{ id: "sample", title: "UX 작성 가이드(샘플)", url, sections: chunk(text, "UX 작성 가이드(샘플)", url) }],
    { space: SPACE, generatedAt: "(sample)", source: "sample" }
  );
  process.exit(0);
}

if (!BASE || !EMAIL || !TOKEN) die("CONFLUENCE_BASE, CONFLUENCE_EMAIL, CONFLUENCE_API_TOKEN 환경변수를 설정하세요.");

const AUTH = "Basic " + Buffer.from(`${EMAIL}:${TOKEN}`).toString("base64");

async function api(q){
  const res = await fetch(BASE + q, { headers: { Authorization: AUTH, Accept: "application/json" } });
  if (!res.ok) die(`HTTP ${res.status} ${res.statusText} — ${q}`);
  return res.json();
}

async function run(){
  const docs = [];
  let start = 0; const limit = 50;
  for (;;){
    const q = `/rest/api/content?spaceKey=${encodeURIComponent(SPACE)}&type=page&status=current` +
              `&limit=${limit}&start=${start}&expand=body.storage`;
    const data = await api(q);
    const results = data.results || [];
    for (const p of results){
      const title = p.title || "(제목 없음)";
      const html = p.body && p.body.storage ? p.body.storage.value : "";
      const webui = p._links && p._links.webui ? p._links.webui : "";
      const url = webui ? (BASE + webui) : BASE;
      const text = htmlToText(html);
      const sections = chunk(text, title, url);
      if (sections.length) docs.push({ id: String(p.id), title, url, sections });
    }
    if (results.length < limit) break;
    start += limit;
  }
  if (!docs.length) die("수집된 페이지가 없습니다. SPACE 키/권한을 확인하세요.");
  writeKB(docs, { space: SPACE, generatedAt: new Date().toISOString(), source: BASE });
}
run().catch(e => die(e && e.message ? e.message : String(e)));
